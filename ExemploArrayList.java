import java.util.ArrayList;

public class ExemploArrayList {
	public static void main(String[] args) {
		ArrayList<String> nomes = new ArrayList<String>();
		
		nomes.add("Beatriz gatinha");
		nomes.add("Pietro");
		nomes.add("Sei lá");
		
		nomes.remove(2);
		
		
		ArrayList<Integer> idades = new ArrayList<Integer>();

		idades.add(17);
		idades.add(28);
		
		
		ArrayList<Double> alturas = new ArrayList<Double>();
		
		alturas.add(1.60);
		alturas.add(1.70);
		
		System.out.println("Os nomes são: " + nomes + "\n\nSuas idades são: " + idades + "\n\nE suas alturas são respectivamente: " + alturas);
	}
}
